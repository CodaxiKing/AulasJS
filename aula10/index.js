/* Aritméticos 
+ Adição / Concatenação (Dentro de '' ele irá concatenar, no exemplo ficaria 510)
- Subtração
/ Divisão
* Multiplicação
** Portenciação
% Resto da divisão
++ Operador de Incremento (Adiciona +1)
-- Operador de decremento (Subtrai -1)
*/

const num1 = 5;
const num2 = 2;
const num3 = 10;
console.log((num1 + num2) * num3);

let contador = 1;
contador++; // 2
contador++; // 3
++contador; // 4
++contador; // 5
console.log(contador);


